const input = document.getElementById('letter');
const display = document.querySelector('button');


function lightOn(str){
	matrix.forEach(item => {
		if(item.includes(str)){
			for(let i = 0; i < item[1].length; i++){
				const pixel = document.getElementById(item[1][i]);
				pixel.style.backgroundColor = 'orange';
			}
		}
	})
}

function resetScr(){
	for (let i = 1; i <= 35; i++){
		const pixel = document.getElementById(i);
		pixel.style.backgroundColor = 'navy';	
	}
}

function stringLightOn(str){
	const singleLetter = n => {
		if(n < str.length){
			resetScr();
			lightOn(str[n]), 1000;
			n++;
			setTimeout(function(){singleLetter(n++);}, 1500);
		}
	}
	singleLetter(0);
}

display.addEventListener('click', () => {
	resetScr();
	const letter = input.value.toUpperCase();
	stringLightOn(letter);
	input.value = '';
});



